bool oled_task_user(void) {
	
    /* KEYBOARD PET VARIABLES START */

    current_wpm   = get_current_wpm();
    led_usb_state = host_keyboard_led_state();

    /* KEYBOARD PET VARIABLES END */	

    oled_write_P(republic_logo, false);
	oled_set_cursor(0,6);
	
    // Host Keyboard Layer Status
    // oled_write_P(PSTR("Layer"), false);

    switch (get_highest_layer(layer_state)) {
        case 0:
            oled_write_P(PSTR("1st\n"), false);
            break;
        case 1:
            oled_write_P(PSTR("2nd\n"), false);
            break;
        case 2:
            oled_write_P(PSTR("3rd\n"), false);
            break;
        default:
            // Or use the write_ln shortcut over adding '\n' to the end of your string
            oled_write_ln_P(PSTR("UNDEF"), false);
    }

	oled_set_cursor(0,8);
    // Host Keyboard LED Status
    led_t led_state = host_keyboard_led_state();
    oled_write_P(led_state.num_lock ? PSTR("NUM\n") : PSTR("\n"), false);
    oled_write_P(led_state.caps_lock ? PSTR("CAP\n") : PSTR("\n"), false);
    oled_write_P(led_state.scroll_lock ? PSTR("SCR\n") : PSTR("\n"), false);
	
	/* wpm counter */
		uint8_t n = get_current_wpm();
		char    wpm_str[4];
		oled_set_cursor(1, 11);
		wpm_str[3] = '\0';
		wpm_str[2] = '0' + n % 10;
		wpm_str[1] = '0' + (n /= 10) % 10;
		wpm_str[0] = '0' + n / 10;
		oled_write(wpm_str, false);

		/* oled_set_cursor(0, 15);
		   oled_write(" wpm", false); */
		
	/* KEYBOARD PET RENDER START */

    render_luna(0, 13);

    /* KEYBOARD PET RENDER END */
    
    return false;
}

